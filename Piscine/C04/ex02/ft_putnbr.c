void ft_putnbr(int n) {
    if (n < 0) {
        putchar('-');
        n = -n;
    }

    if (n >= 10) {
        putnbr(n / 10);
    }

    putchar(n % 10 + '0');
}

int main() {
    int number = 12345;
    putnbr(number);
    putchar('\n');
    return 0;
}