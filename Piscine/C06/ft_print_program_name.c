void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		write(1, &str[i], 1);
		i ++;
	}
}
int main(int argc, char const *argv[])
{
	ft_putstr(*argv);
	return 0;
}